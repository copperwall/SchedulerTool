package data;

import java.util.Collection;

/**
 * Location holds many fields of a Location. A Location holds the building
 * number (14), room number (256), if it has equipment (such as lab machines),
 * and its capacity (36). Get and set methods are included for each class
 * variable.
 */
public abstract class Location {
   private int buildingNumber;
   private String roomNumber;
   private boolean hasEquipment;
   private int capacity;
   private Collection<Day> availability;
   
   public abstract int getBuildingNum();
   public abstract int getRoomNum();
}
